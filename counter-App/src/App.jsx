import React, { createElement, useState } from "react"
// import "/App.css"
// import NewComponent from "./NewComponent.jsx"
 

 
function App() {
  let num = 40
  let [a, b] = useState(0)
  return (
 
    <>
      <h1 id="number">{a}</h1>
      <button onClick={
        ()=>{
          b(a + 1)   
        }
      }>Add</button>
 
      <button onClick={
        ()=>{
          if(a === 0){
            b(a = 0)
            console.log(`negative values are not acceptable`);
          }else{
            b(a - 1)
          }
        }
      }>SUB</button>
    </>
  )
}
 
export default App

